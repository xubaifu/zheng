package com.zheng.upms.server.controller.demo;

public class DemoEntity {

	private String name;
	private String level;
	private String mideaNum;
	private String mideaPercent;
	private String panasonicNum;
	private String panasonicPercent;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getMideaNum() {
		return mideaNum;
	}
	public void setMideaNum(String mideaNum) {
		this.mideaNum = mideaNum;
	}
	public String getMideaPercent() {
		return mideaPercent;
	}
	public void setMideaPercent(String mideaPercent) {
		this.mideaPercent = mideaPercent;
	}
	public String getPanasonicNum() {
		return panasonicNum;
	}
	public void setPanasonicNum(String panasonicNum) {
		this.panasonicNum = panasonicNum;
	}
	public String getPanasonicPercent() {
		return panasonicPercent;
	}
	public void setPanasonicPercent(String panasonicPercent) {
		this.panasonicPercent = panasonicPercent;
	}
}
